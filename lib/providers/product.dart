// Model of Product
import 'package:flutter/foundation.dart';

class Product with ChangeNotifier {
  final String id;
  final String title;
  final String description;
  final  price;
  final String imageUrl;
  final String publisherid;
  final invitations;

  Product({
    @required this.id,
    @required this.title,
    @required this.description,
    @required this.price,
    @required this.imageUrl,
    this.publisherid,
    this.invitations,
  });
}
